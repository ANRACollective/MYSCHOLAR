  s.bumi_only                 AS "bumiOnly",
  s.open_to_all               AS "openAll",
  s.foreigner_ok              AS "foreignerOk",
  s.is_global                 AS global,
  s.rolling,
  s.deadline_text             AS dl,
  s.deadline_months           AS dl_months,
  s.open_months               AS dl_open,
  s.grade_minimum::text       AS "gradeMin",
  s.grade_description         AS grade,
  s.notes,
  s.url,
  s.last_verified,
  -- Aggregated arrays (match old JS field names)
  COALESCE(
    array_agg(DISTINCT el.entry_level::text)
    FILTER (WHERE el.entry_level IS NOT NULL), ARRAY[]::text[]
  ) AS entry,
  COALESCE(
    array_agg(DISTINCT sf.field_tag::text)
    FILTER (WHERE sf.field_tag IS NOT NULL), ARRAY[]::text[]
  ) AS "fieldTags",
  COALESCE(
    array_agg(DISTINCT sf.field_display)
    FILTER (WHERE sf.field_display IS NOT NULL), ARRAY[]::text[]
  ) AS fields,
  COALESCE(
    array_agg(DISTINCT sq.qualification::text)
    FILTER (WHERE sq.qualification IS NOT NULL), ARRAY[]::text[]
  ) AS quals
FROM scholarships s
LEFT JOIN scholarship_entry_levels el ON el.scholarship_id = s.id
LEFT JOIN scholarship_fields sf       ON sf.scholarship_id = s.id
LEFT JOIN scholarship_quals sq        ON sq.scholarship_id = s.id
WHERE s.is_active = true
GROUP BY s.id
ORDER BY s.id;

