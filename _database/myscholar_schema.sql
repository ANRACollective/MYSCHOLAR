SELECT
  s.id,
  s.name,
  s.provider,
  s.category::text            AS cat,
  s.amount_description        AS amt,
  s.coverage::text            AS cov,
  s.destination::text         AS dest,
  s.bond_description          AS bond,
  s.no_bond                   AS nb,
